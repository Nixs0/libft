char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	d;

	i = 0;
	d = (char)c;
	while (s[i] != d)
	{
		if (!s[i])
			return (0);
		i++;
	}
	return ((char *)&s[i]);
}
